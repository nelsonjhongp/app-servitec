    public void tituloAnimado(){
        Font anotherfont = CustomFont.loadCustomAnotherFont(36f);
        if (anotherfont != null) {
            tituloElegir.setFont(anotherfont);
        }
        
        // Texto que deseas mostrar
        final String textoCompleto = "Â¿Que tipo de Usuario Registrar?";
        final int delay = 20; // Intervalo en milisegundos entre letras

        Timer timer = new Timer(delay, new ActionListener() {
            int index = 0;

            @Override
            public void actionPerformed(ActionEvent e) {
                if (index < textoCompleto.length()) {
                    tituloElegir.setText(textoCompleto.substring(0, index + 1));
                    index++;
                } else {
                    ((Timer) e.getSource()).stop(); // Detener el temporizador cuando se haya mostrado todo el texto
                }
            }
        });

        timer.start();
    }
    
